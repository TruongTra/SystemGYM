    private void jButton10ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton10ActionPerformed
        // TODO add your handling code here:
        if (txtChuDe.getText().length() == 0) {
            lblTrangThai.setForeground(Color.red);
            lblTrangThai.setText("KHÔNG ĐỂ TRỐNG");

        } else if (txtNoiDung.getText().length() == 0) {
            lblTrangThai.setForeground(Color.red);
            lblTrangThai.setText("KHÔNG ĐỂ TRỐNG");
        } else if (txtFileName.getText().length() == 0) {
            lblTrangThai.setForeground(Color.red);
            lblTrangThai.setText("KHÔNG ĐỂ TRỐNG");
        } else {
            try {
                Properties p = new Properties();
                p.put("mail.smtp.auth", "true");
                p.put("mail.smtp.starttls.enable", "true");
                p.put("mail.smtp.host", "smtp.gmail.com");
                p.put("mail.smtp.port", 587);

                String accountName = taikhoan;
                String accountPassword = matkhau;
                Session s = Session.getInstance(p,
                        new javax.mail.Authenticator() {
                    protected javax.mail.PasswordAuthentication getPasswordAuthentication() {
                        return new javax.mail.PasswordAuthentication(accountName, accountPassword);
                    }
                });
                String from = taikhoan;
                String to = nguoinhan;
                String subject = txtChuDe.getText();
                String body = txtNoiDung.getText();
                Message msg = new MimeMessage(s);
                msg.setFrom(new InternetAddress(from));
                msg.setRecipients(Message.RecipientType.TO, InternetAddress.parse(to));
                msg.setSubject(subject);
                msg.setText(body);
                DataSource source = new FileDataSource(filename);
                msg.setDataHandler(new DataHandler(source));
                msg.setFileName(file);

                Transport.send(msg);
                lblTrangThai.setForeground(Color.green);
                lblTrangThai.setText("GỬI THÀNH CÔNG");
            } catch (MessagingException ex) {
                lblTrangThai.setForeground(Color.red);
                lblTrangThai.setText("GỬI THẤT BẠI");
            }
        }
    }//GEN-LAST:event_jButton10ActionPerformed

