    public void showTableAccount() {
        DefaultTableModel modelAC = (DefaultTableModel) tblTaiKhoan.getModel();
        EmailDAO eDAO = new EmailDAO();
        ArrayList<Email> dsEmail = eDAO.loadList();
        modelAC.setRowCount(0);
        for (Email e : dsEmail) {
            Vector row = new Vector();
            row.add(e.getTaikhoan());

            modelAC.addRow(row);
        }
        tblTaiKhoan.setModel(modelAC);
    }

    public void showTableEmail() {
        DefaultTableModel modelE = (DefaultTableModel) tblEmail.getModel();
        EmailDAO eDAO = new EmailDAO();
        ArrayList<DSEmail> dsEmail = eDAO.loadListEmail();
        modelE.setRowCount(0);
        for (DSEmail dse : dsEmail) {
            Vector row = new Vector();
            row.add(dse.getHovaten());
            row.add(dse.getEmail());

            modelE.addRow(row);
        }
        tblEmail.setModel(modelE);
    }

